# Jogo da Forca
# Descrição: Escreva um programa que simule o jogo da forca. O programa deve escolher uma palavra aleatoriamente de uma lista e permitir que o usuário adivinhe letras. Use condicionais para verificar se a letra adivinhada está na palavra, se já foi adivinhada antes e para determinar quando o jogo termina (vitória ou derrota).

